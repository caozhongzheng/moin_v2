package com.moinapp.wuliao.commons.net;

public class DomainProviderFactory {

    // ===========================================================
    // Constants
    // ===========================================================

    // ===========================================================
    // Fields
    // ===========================================================

    private static IDomainProvider sProvider;

    // ===========================================================
    // Constructors
    // ===========================================================

    // ===========================================================
    // Getter & Setter
    // ===========================================================

    // ===========================================================
    // Methods for/from SuperClass/Interfaces
    // ===========================================================

    // ===========================================================
    // Methods
    // ===========================================================

    public static void setProvider(IDomainProvider provider){
        sProvider = provider;
    }

    public static IDomainProvider getProvider(){
        if(sProvider != null){
            return sProvider;
        }else{
            return new DefaultDomainProvider();
        }
    }

    // ===========================================================
    // Inner and Anonymous Classes
    // ===========================================================
}
