    public static String[] fomatTime(long millions){
        Calendar ca = Calendar.getInstance();
        ca.setTimeInMillis(millions);
        String[] result = new String[2];
        result[0] = String.valueOf(ca.get(Calendar.DAY_OF_MONTH));
        result[1] = (ca.get(Calendar.MONTH) + 1) +  "|" + ca.get(Calendar.YEAR);
        return result;
    }

